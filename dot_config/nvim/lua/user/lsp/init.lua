-- Highlight symbol under cursor
local function lsp_document_heightlight(client)
	if client.resolved_capabilities.document_highlight then
    vim.cmd [[
      hi! LspReferenceRead cterm=bold ctermbg=red guibg=LightYellow
      hi! LspReferenceText cterm=bold ctermbg=red guibg=LightYellow
      hi! LspReferenceWrite cterm=bold ctermbg=red guibg=LightYellow
      augroup lsp_document_highlight
        autocmd! * <buffer>
        autocmd! CursorHold <buffer> lua vim.lsp.buf.document_highlight()
        autocmd! CursorMoved <buffer> lua vim.lsp.buf.clear_references()
      augroup END
    ]]
	end
end
-- See `:help vim.diagnostic.*` for documentation on any of the below functions
-- Use an on_attach function to only map the following keys
-- after the language server attaches to the current buffer
local on_attach = function(client, bufnr)
	-- Disable default formatting
  if client.name == "tsserver" then
    client.resolved_capabilities.document_formatting = false
  end

	lsp_keymaps(bufnr)
	lsp_document_heightlight(client)

end

-- LSP settings (for overriding per client)
local handlers =  {
  ["textDocument/hover"] =  vim.lsp.with(vim.lsp.handlers.hover, {border = "rounded"}),
  ["textDocument/signatureHelp"] =  vim.lsp.with(vim.lsp.handlers.signature_help, {border = "rounded" }),
}

-- Add additional capabilities supported by nvim-cmp
local capabilities = vim.lsp.protocol.make_client_capabilities()
capabilities = require('cmp_nvim_lsp').update_capabilities(capabilities)

-- Use a loop to conveniently call 'setup' on multiple servers and
-- map buffer local keybindings when the language server attaches
local servers = {'sumneko_lua','jsonls','rust_analyzer', 'tsserver' }

for _, lsp in pairs(servers) do

	-- default opts
	local opts = {
		on_attach = on_attach,
		handlers = handlers,
		capabilities = capabilities,
    flags = {
      -- This will be the default in neovim 0.7+
      debounce_text_changes = 150,
    },
	}

	local settingPath = 'user.lsp.settings.' .. lsp

	local have_setting, setting = pcall(require, settingPath)

	if have_setting then
		opts = vim.tbl_deep_extend("force", setting, opts)
	end

  lspconfig[lsp].setup(opts)

end


